package com.xworkz.generalaptitude;

public class PrimeNumberUsingForLoop {
	public static void main(String args[]) {
		int num=4  ;
		for(int i=2;i< num;i++) {
			
			if(num%i==0) {
			System.out.println("given number not prime ");
			break;
			}
			else {
				System.out.println("given number is prime");
			break;
			
			
			
		}	
		
	}
	}
}
